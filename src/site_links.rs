pub fn get_reddit_url() -> String {
    let reddit_url: String = "https://www.reddit.com/".to_string();
    reddit_url
}
pub fn get_arxiv_url() -> String {
    let arxiv_url: String = "http://export.arxiv.org/rss/".to_string();
    arxiv_url
}
pub fn get_biorxiv_url() -> String {
    let biorxiv_url: String = "http://connect.biorxiv.org/".to_string();
    biorxiv_url
}
pub fn get_medrxiv_url() -> String {
    let medrxiv_url: String = "http://connect.medrxiv.org/".to_string();
    medrxiv_url
}
